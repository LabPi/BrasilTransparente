<p>
Brasil Transparente é o resultado da provocação lançada pelo Ministério da Justiça, em edital, cujo objetivo é a disponibilização de software que atue no enfrentamento da corrupção e proporcione interação da sociedade civil quanto a às informações referentes a transferências de recursos e convênios do governo quando da execução de obras, projetos e eventos.

Brasil Transparente, aceitando esse desafio, é o protótipo que apresenta como princípios

SIMPLICIDADE a base do Brasil Transparente na relação do usuário com os dados, levando o cidadão à sua interpretação quase automática quanto à interpretação das informações apresentadas por meio dos dados disponibilizados. 

TRANSPARÊNCIA para revelar a realidade brasileira por trás dos dados que o cidadão quiser, sem ocultar as realidades e partes envolvidas. 

INTERAÇÃO habitua o usuário ao acompanhamento, fiscalização e denúncia quanto ao andamento de obras, execução de projetos e eventos, por meio de transferências de recursos e convênios em sua localidade, em esfera federal, estadual, distrital ou municipal. Bem como possibilita ao governo se utilizar das informações fornecidas pelo usuário com relação a sua realidade.

CONHECIMENTO LIVRE sustentado sob as premissas de liberdade da licença GNU, permitindo acesso aos código-fonte e garantindo a possibilidade ao seu aperfeiçoamento.  Além, é claro, da disponibilização de conhecimento e dados, de forma instantânea e livre, sem burocracias.
</p>
    
